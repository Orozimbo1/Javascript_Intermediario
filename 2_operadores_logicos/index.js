/**
 * OPERADORES LÓGICOS
 * && -> AND -> E
 * || -> OR -> OU
 * !  -> NOT -> NÃO
 */
/**
 * REGRA LOGICA
 * 
 * OPERADOR &&
 * 
 * TRUE && TRUE -> TRUE
 * TRUE && FALSE -> FALSE
 * FALSE && TRUE -> FALSE
 * FALSE && FALSE -> FALSE
 * 
 * OPERADOR ||
 * 
 * TRUE || TRUE -> TRUE
 * TRUE || FALSE -> TRUE
 * FALSE || TRUE -> TRUE
 * FALSE || FALSE -> FALSE
 * 
 * OPERADOR !
 * 
 * !TRUE -> FALSE
 * !FALSE -> TRUE
 */

console.log(true && true) //-> TRUE
console.log(true && false)//-> FALSE
console.log(true || true)//-> TRUE
console.log(true || false)//-> TRUE
console.log(!false)//-> TRUE
console.log(!true)//-> FALSE