/**
 * OPERADORES DE COMPARAÇÃO
 * >    Maior que
 * >=   Maior ou igual que
 * <    Menor que
 * <=   Menor ou igual que
 * ==   Igualdade (valor)
 * ===  Estritamente igual (valor e tipo)
 * !=   Diferente (valor)
 * !==  Estritamente diferente (valor e tipo)
 * SEMPRE RETORNA UM BOOLEAN (TRUE OU FALSO)
 */

console.log(10 > 5) // TRUE
console.log(10 >= 5) // TRUE
console.log(10 < 5) // FALSE
console.log(10 <= 5) // FALSE
console.log(5 == '5') // TRUE
console.log(5 === '5') // FALSE, sao do mesmo valor mas um e NUMBER e o outro STRING
console.log(5 != '5') // FALSE, sao do mesmo valor
console.log(5 !== '5') // TRUE, sao do mesmo valor mas um e NUMBER e o outro STRING